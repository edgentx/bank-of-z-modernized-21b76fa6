package com.example.steps;

import com.example.domain.defect.model.DefectAggregate;
import com.example.domain.defect.model.ReportDefectCmd;
import io.cucumber.java.en.Given;
import io.cucumber.java.en.When;
import io.cucumber.java.en.Then;
import static org.junit.jupiter.api.Assertions.*;

public class VW454Steps {

    private DefectAggregate defectAggregate;
    private Exception capturedException;
    private String resultingGitHubUrl;

    @Given("a defect report is triggered via temporal-worker exec")
    public void a_defect_report_is_triggered() {
        // Setup simulation
        String defectId = "VW-454";
        defectAggregate = new DefectAggregate(defectId);
    }

    @When("the system processes the defect report command")
    public void the_system_processes_the_defect_report_command() {
        ReportDefectCmd cmd = new ReportDefectCmd(
            "VW-454",
            "GitHub URL in Slack body",
            "Verify URL is present",
            "LOW",
            "21b76fa6-afb6-4593-9e1b-b5d7548ac4d1"
        );

        try {
            var events = defectAggregate.execute(cmd);
            // Assuming aggregate state updates immediately for this test context
            // or we could read from the event, but the defect is about the slack body content.
            if (!events.isEmpty()) {
                // In a real scenario, we'd have a projection. Here we check the aggregate state directly
                // or the event payload. The requirement is "Slack body includes GitHub issue: <url>"
                // This implies the URL generated by the aggregate must be valid.
                resultingGitHubUrl = defectAggregate.getGithubUrl();
            }
        } catch (Exception e) {
            capturedException = e;
        }
    }

    @Then("the Slack body contains the GitHub issue link")
    public void the_slack_body_contains_the_github_issue_link() {
        assertNotNull(resultingGitHubUrl, "GitHub URL should not be null");
        assertTrue(resultingGitHubUrl.startsWith("https://github.com/"), "URL should be a valid GitHub link");
        assertTrue(resultingGitHubUrl.contains("VW-454"), "URL should contain the defect ID");
        
        assertNull(capturedException, "Should not have thrown an exception");
    }

    @Then("the validation passes successfully")
    public void the_validation_passes_successfully() {
        the_slack_body_contains_the_github_issue_link();
    }
}
